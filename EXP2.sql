  Book_ID INT PRIMARY KEY,
  Title VARCHAR(50),
  Language_ID INT,
  MRP INT,
  Publisher_ID INT ,
  Publisher_Date DATE,
  Volume INT,
  Status VARCHAR(50),
  FOREIGN KEY (Language_ID) references LANGUAGE_TABLE(Language_ID),
  FOREIGN KEY (Publisher_ID) references PUBLISHER(Publisher_ID) 
);
